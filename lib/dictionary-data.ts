export function getWordByEnglish(english: string): DictionaryWord | undefined {
  return dictionaryData.find((word) => word.english.toLowerCase() === english.toLowerCase())
}

export function getWordById(id: number): DictionaryWord | undefined {
  return dictionaryData.find((word) => word.id === id)
}

export function searchWords(query: string): DictionaryWord[] {
  if (!query) return dictionaryData
  const lowercaseQuery = query.toLowerCase()
  return dictionaryData.filter(
    (word) =>
      word.bangla.toLowerCase().includes(lowercaseQuery) ||
      word.english.toLowerCase().includes(lowercaseQuery) ||
      word.pronunciation.toLowerCase().includes(lowercaseQuery) ||
      word.synonyms.some((synonym) => synonym.toLowerCase().includes(lowercaseQuery)),
  )
}
